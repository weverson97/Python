def somar(a, b):
    return a + b

def subtrair(a, b):
    return a - b

def dividir(a, b):
    if b != 0:
        return a / b

def multiplicar(a, b):
    return a * b

resul = somar(1, 2)
print(resul)

resul = subtrair(1, 2)
print(resul)

resul = multiplicar(3, 2)
print(resul)

resul = dividir(3, 2)
print(resul)