lista = ["Weverson", "Wallace", "Marilene", "Jessica"]

# Exibe lista atual
print(lista)

# Remove o ultimo da lista
lista.pop()

# Exibe lista atual
print(lista)

# Remove a posição desejada, nesse caso, a posição 0
lista.pop(0)

# Exibe lista atual
print(lista)

# Remove o nome na lista se encontrar 
lista.remove("Marilene")

# Exibe lista atual
print(lista)

# Adiciona no final da lista
lista.append("Weverson")

# Exibe lista atual
print(lista)

# Adiciona na posição desejada da lista, nesse caso, na primeira posição
lista.insert(0, "Jessica")

# Exibe lista atual
print(lista)